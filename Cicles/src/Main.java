public class Main
{
    public static void main(String[] args)
    {
        //for
        for (int i = 200000; i <= 210000 ; i ++)
        {
            System.out.println("Ticket number is: " + i);
        }
        for (int i = 220000; i <= 235000 ; i ++)
        {
            System.out.println("Ticket number is: " + i);
        }

        //============================================================
        //while
        int i = 200000;
        while (i <= 210000)
        {
            System.out.println("Ticket number is: " + i);
            i++;
        }
        i = 220000;
        while (i <= 235000)
        {
            System.out.println("Ticket number is: " + i);
            i++;
        }

    }
}
